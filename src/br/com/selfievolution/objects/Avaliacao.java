package br.com.selfievolution.objects;


public class Avaliacao {

	private int id, idade, dobras;
	private double peso;

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public int getDobras() {
		return dobras;
	}

	public void setDobras(int dobras) {
		this.dobras = dobras;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

}
